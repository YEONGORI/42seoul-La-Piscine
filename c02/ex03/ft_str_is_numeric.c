int	ft_str_is_numeric(char *str)
{
	int	cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (str[cnt] < 47 || str[cnt] > 58)
			return (0);
		else
			cnt++;
	}
	if (cnt == 0)
		return (1);
	return (1);
}
