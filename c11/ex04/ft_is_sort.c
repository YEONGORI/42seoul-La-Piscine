int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int	i;
	int	ch[2];

	i = 1;
	ch[0] = 0;
	ch[1] = 0;
	while (i < length)
	{
		if (f(tab[i - 1], tab[i]) < 0)
			ch[0] = 1;
		else if (f(tab[i - 1], tab[i] > 0))
			ch[1] = 1;
		i++;
	}
	if (ch[0] == 1 && ch[1] == 1)
		return (0);
	return (1);
}
